    //getting the input from the checkup textboxes and adding to the appoitment type list
    private void addCheckUpButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addCheckUpButtonActionPerformed
        // TODO add your handling code here:
        try{
            int  duration = Integer.parseInt(this.checkUpDurationText.getText());

            if(this.noPolishRadio.isSelected()) checkup = new CheckUp(duration, false);
            else checkup = new CheckUp(duration, true);;
            appointmentTypeList.add(checkup);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Enter the duration, as number");
        }
    }//GEN-LAST:event_addCheckUpButtonActionPerformed

