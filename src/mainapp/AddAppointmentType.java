    //getting the input from the treatment type textboxes and adding to the appoitment type list
    private void addTreatmentTypeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addTreatmentTypeButtonActionPerformed
        // TODO add your handling code here:
        
        try{
            String desc = this.treatmentDescText.getText();
            if(desc == "")desc = this.treatmentTypeCombo.getSelectedItem().toString();
            int duration = Integer.parseInt(this.treatmentDurationText.getText());
            double cost = Double.parseDouble(this.treatmentCostText.getText());

            treatment = new Treatment(desc, duration, cost);
            appointmentTypeList.add(treatment);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,"Enter the duration and cost, as numbers");
        }
        
    }//GEN-LAST:event_addTreatmentTypeButtonActionPerformed

